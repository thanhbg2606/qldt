package model;

import java.io.Serializable;

/**
 *
 * @author NgocThanh
 */
public class HocKi implements Serializable{
    private int id;
    private String ten;
    private String mota;

    public HocKi() {
    }

    public HocKi(String ten, String mota) {
        this.ten = ten;
        this.mota = mota;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }  
}
